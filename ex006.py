#Instrução: Faça a media entre quatro notas de um aluno
print('A seguir digite as quatro notas do seu aluno')
n1 = float(input('Digite a primeira nota dele: '))
n2 = float(input('Digite a segunda nota dele: '))
n3 = float(input('Digite a terceira nota dele: '))
n4 = float(input('Digite a quarta nota dele: '))
print('A média dele foi {}!'.format((n1+n2+n3+n4)/4))
